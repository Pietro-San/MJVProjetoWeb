package br.com.mjv.model;

public class Usuario {
	//Atributos
	private int id;
	private String user;
	//Getters e Setters
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}	
}
